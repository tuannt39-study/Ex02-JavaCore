package vn.its;

public class OldCar {
	
	private int actionDuration;

	public OldCar() {
		super();
	}

	public OldCar(int actionDuration) {
		super();
		this.actionDuration = actionDuration;
	}

	public int getActionDuration() {
		return actionDuration;
	}

	public void setActionDuration(int actionDuration) {
		this.actionDuration = actionDuration;
	}

}
